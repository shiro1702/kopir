export function formatPriceRub(kopeks: number): string {
  return (kopeks / 100).toFixed(kopeks % 100 === 0 ? 0 : 2).replace(/\.00$/, '')
}

export function formatHomePricingText(priceRub: string): string {
  return HOME_PRICING.template.replace('{price}', priceRub)
}
